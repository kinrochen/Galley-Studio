import { createHash } from "node:crypto";
import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";

const PINNED_VERSION = "1.0.12";
const root = fileURLToPath(new URL("../", import.meta.url));
const packageJsonPath = new URL("../node_modules/hugerte/package.json", import.meta.url);
const skinPath = new URL(
  "../node_modules/hugerte/skins/ui/oxide/skin.min.css",
  import.meta.url
);
const contentPath = new URL(
  "../node_modules/hugerte/skins/content/default/content.min.css",
  import.meta.url
);
const outputPath = new URL("../src/generated/hugerteSkin.ts", import.meta.url);
const stylesPath = new URL("../styles.css", import.meta.url);
const SKIN_START = "/* BEGIN GALLEY STUDIO BUNDLED HUGERTE SKIN */";
const SKIN_END = "/* END GALLEY STUDIO BUNDLED HUGERTE SKIN */";
const requestedVersion = process.argv
  .find((argument) => argument.startsWith("--expected-version="))
  ?.slice("--expected-version=".length);
const expectedVersion = requestedVersion || PINNED_VERSION;
const approvedFontStack = 'Inter,"Noto Sans SC","Noto Sans",sans-serif';
const upstreamSystemFontStack = /-apple-system,BlinkMacSystemFont,(?:"Segoe UI"|'Segoe UI'),Roboto,(?:Oxygen-Sans|Oxygen),Ubuntu,Cantarell,(?:(?:"Open Sans"|'Open Sans'),)?(?:"Helvetica Neue"|'Helvetica Neue'),sans-serif/gu;

const packageJson = JSON.parse(await readFile(packageJsonPath, "utf8"));
if (packageJson.version !== expectedVersion) {
  throw new Error(
    `Expected hugerte ${expectedVersion}, found ${String(packageJson.version)} at ${fileURLToPath(packageJsonPath)}`
  );
}
if (packageJson.version !== PINNED_VERSION) {
  throw new Error(`Generator only supports pinned hugerte ${PINNED_VERSION}`);
}

const skinCss = normalizeCss(await readFile(skinPath, "utf8"), skinPath);
const contentCss = normalizeCss(await readFile(contentPath, "utf8"), contentPath);
const skinHash = sha256(skinCss);
const contentHash = sha256(contentCss);
const generated = [
  "// AUTO-GENERATED by tools/embed-hugerte-assets.mjs. DO NOT EDIT.",
  `// Source: hugerte@${PINNED_VERSION}/skins/ui/oxide/skin.min.css sha256:${skinHash}`,
  `// Source: hugerte@${PINNED_VERSION}/skins/content/default/content.min.css sha256:${contentHash}`,
  `export const HUGERTE_SKIN_VERSION = ${JSON.stringify(PINNED_VERSION)};`,
  `export const HUGERTE_SKIN_SHA256 = ${JSON.stringify(skinHash)};`,
  `export const HUGERTE_CONTENT_SHA256 = ${JSON.stringify(contentHash)};`,
  `export const HUGERTE_INLINE_SKIN_CSS = ${JSON.stringify(skinCss)};`,
  `export const HUGERTE_CONTENT_CSS = ${JSON.stringify(contentCss)};`,
  ""
].join("\n");

await writeFile(outputPath, generated, "utf8");
const currentStyles = await readFile(stylesPath, "utf8");
const skinSection = new RegExp(
  `${escapeRegExp(SKIN_START)}[\\s\\S]*?${escapeRegExp(SKIN_END)}\\n?`,
  "u"
);
const baseStyles = currentStyles.replace(skinSection, "").trimEnd();
await writeFile(
  stylesPath,
  `${baseStyles}\n\n${SKIN_START}\n${skinCss}${SKIN_END}\n`,
  "utf8"
);
process.stdout.write(
  `Embedded hugerte@${PINNED_VERSION} CSS from ${root} (${skinCss.length} UI bytes, ${contentCss.length} content bytes)\n`
);

function normalizeCss(source, sourcePath) {
  const normalized = source
    .replace(/\r\n?/gu, "\n")
    .trim()
    .replace(upstreamSystemFontStack, approvedFontStack);
  if (normalized.includes("-apple-system") || normalized.includes("BlinkMacSystemFont")) {
    throw new Error(`Unrecognized proprietary system font stack in ${fileURLToPath(sourcePath)}`);
  }
  if (/(?:@import\s+[^;]*(?:https?:)?\/\/|url\(\s*["']?(?:https?:)?\/\/)/iu.test(normalized)) {
    throw new Error(`Remote CSS asset found in ${fileURLToPath(sourcePath)}`);
  }
  if (!normalized.includes(approvedFontStack)) {
    throw new Error(`Approved font stack was not embedded from ${fileURLToPath(sourcePath)}`);
  }
  return `${normalized}\n`;
}

function sha256(value) {
  return createHash("sha256").update(value, "utf8").digest("hex");
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/gu, "\\$&");
}

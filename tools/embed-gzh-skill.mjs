import { execFileSync } from "node:child_process";
import { createHash } from "node:crypto";
import {
  readdirSync,
  readFileSync,
  realpathSync,
  mkdirSync,
  statSync,
  writeFileSync
} from "node:fs";
import { dirname, join, relative, resolve, sep } from "node:path";
import { fileURLToPath } from "node:url";

import { zipSync } from "fflate";

const SKILL_ID = "gzh-design";
const OUTPUT_PATH = resolve(
  dirname(fileURLToPath(import.meta.url)),
  "../src/generated/bundledSkill.ts"
);
const ZIP_TIMESTAMP = new Date(1980, 0, 1, 0, 0, 0);

function parseArguments(args) {
  const parsed = {};

  for (let index = 0; index < args.length; index += 2) {
    const flag = args[index];
    const value = args[index + 1];
    if ((flag !== "--source" && flag !== "--version") || !value) {
      throw new Error("Usage: embed-gzh-skill.mjs --source <directory> --version <sha>");
    }
    parsed[flag.slice(2)] = value;
  }

  if (!parsed.source || !parsed.version || !/^[a-f0-9]{40}$/.test(parsed.version)) {
    throw new Error("Usage: embed-gzh-skill.mjs --source <directory> --version <sha>");
  }

  return parsed;
}

function git(source, args) {
  return execFileSync("git", ["-C", source, ...args], {
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"]
  }).trim();
}

function verifySource(source, version) {
  const repositoryRoot = realpathSync(git(source, ["rev-parse", "--show-toplevel"]));
  if (repositoryRoot !== source) {
    throw new Error("Skill source must be the Git repository root");
  }

  const actualVersion = git(source, ["rev-parse", "HEAD"]);
  if (actualVersion !== version) {
    throw new Error(
      `Skill source version mismatch: expected ${version}, received ${actualVersion}`
    );
  }

  const status = git(source, [
    "status",
    "--porcelain=v1",
    "--untracked-files=all",
    "--ignored=matching"
  ]);
  if (status) {
    throw new Error("Skill source must be a clean checkout of the pinned commit");
  }
}

function collectRegularFiles(source) {
  const paths = [];

  function visit(directory) {
    for (const entry of readdirSync(directory, { withFileTypes: true })) {
      const absolutePath = join(directory, entry.name);
      const path = relative(source, absolutePath).split(sep).join("/");

      if (path === ".git" || path.startsWith(".git/") || path === ".github" || path.startsWith(".github/")) {
        continue;
      }

      if (entry.isDirectory()) {
        visit(absolutePath);
      } else if (entry.isFile() && statSync(absolutePath).isFile()) {
        paths.push(path);
      }
    }
  }

  visit(source);
  return paths.sort();
}

function createArchive(source, paths) {
  const entries = {};
  for (const path of paths) {
    entries[path] = [
      new Uint8Array(readFileSync(join(source, ...path.split("/")))),
      { level: 9, mtime: ZIP_TIMESTAMP }
    ];
  }

  return zipSync(entries);
}

function renderModule(version, paths, archive) {
  const sha256 = createHash("sha256").update(archive).digest("hex");
  const base64 = Buffer.from(archive).toString("base64");

  return `// Generated by tools/embed-gzh-skill.mjs from the pinned upstream checkout.\n` +
    `// gzh-design-skill is licensed under AGPL-3.0; see THIRD_PARTY_NOTICES.md.\n` +
    `export const BUNDLED_SKILL = Object.freeze({\n` +
    `  id: ${JSON.stringify(SKILL_ID)},\n` +
    `  version: ${JSON.stringify(version)},\n` +
    `  archiveSha256: ${JSON.stringify(sha256)},\n` +
    `  files: Object.freeze(${JSON.stringify(paths, null, 2)}),\n` +
    `  archiveBase64: ${JSON.stringify(base64)}\n` +
    `});\n`;
}

function main() {
  const { source: sourceArgument, version } = parseArguments(process.argv.slice(2));
  const source = realpathSync(resolve(sourceArgument));
  verifySource(source, version);

  const paths = collectRegularFiles(source);
  const archive = createArchive(source, paths);
  mkdirSync(dirname(OUTPUT_PATH), { recursive: true });
  writeFileSync(OUTPUT_PATH, renderModule(version, paths, archive), "utf8");
}

main();
